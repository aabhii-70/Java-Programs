public class SwitchStatement {
    public static void main(String[] args) {
        int num = 5;//char ch
        switch(num){//switch(ch){
            case 2 : System.out.println("Samosa"); //'a'
            break;
            case 1 : System.out.println("Burger");//'c'
            break;
            case 3 : System.out.println("Mango Shake");//'b'
            break;
            default : System.out.println("Wake Up");
        }
    }
}
