public class RelationalOperators {
    public static void main(String[] args) {
        int A = 10;
        int B = 5;
        int num1 = 10;
        int num2 = 10;
        //equal to
        System.out.println((A == B));
        System.out.println((num1 == num2));
        //!= (not equal to)
        System.out.println((num1 != num2));
        //> (greater than)
        System.out.println((A > B));
        //< (less than)
        System.out.println((A < B));
        //>= (greater than equal to) <= (less than equal to)
        System.out.println((num1 >= num2));
        System.out.println((num1 <= num2));
    }
}
