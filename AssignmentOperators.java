public class AssignmentOperators {
    public static void main(String[] args) {
        //Assignment Operator

        int A = 10;
        //A = A + 10;
        A += 10;
        System.out.println(A);

        int B = 5;
        //B = B - 5;
        B -= 5; 
        System.out.println(B);

        int C = 10;
        //C = C * 5;
        C *= 5;
        System.out.println(C);

        int D = 10;
        //D = D / 2;
        D /= 2;
        System.out.println(D);

        int E = 27;
        //E = E % 4;
        E %= 4;
        System.out.println(E);
    }
}
